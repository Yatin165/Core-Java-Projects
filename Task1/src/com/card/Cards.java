package com.card;

public class Cards {
	
	private String name;
	private String phoneNumber;
	
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setNumber(String number) {
		this.phoneNumber = number;
	}
	
	
	public String getName() {
		return this.name;
	}
	
	public String getNumber() {
		return this.phoneNumber;
	}
	
	

}
